-- ---------- HOLDINGS (derived from transactions) ----------
-- Holdings are computed from transaction history:
-- net quantity = SUM(BUY qty) - SUM(SELL qty)
-- avg_buy_price = weighted average price of BUY transactions only
INSERT INTO holdings (portfolio_id, stock_id, quantity, avg_buy_price)
SELECT
    t.portfolio_id,
    t.stock_id,
    SUM(CASE WHEN t.transaction_type = 'BUY' THEN t.quantity ELSE -t.quantity END) AS net_qty,
    ROUND(
        SUM(CASE WHEN t.transaction_type = 'BUY' THEN t.quantity * t.price_per_share ELSE 0 END)
        / NULLIF(SUM(CASE WHEN t.transaction_type = 'BUY' THEN t.quantity ELSE 0 END), 0)
    , 2) AS avg_buy_price
FROM transactions t
GROUP BY t.portfolio_id, t.stock_id
HAVING net_qty > 0;
